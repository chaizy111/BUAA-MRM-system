class PatientInfo:
    def __init__(self, name, gender, birth_date, age, nationality, place_of_birth, ethnicity,
                 id_card_number, occupation, marital_status, current_address, phone,
                 postal_code, household_address):
        self.name = name
        self.gender = gender
        self.birth_date = birth_date
        self.age = age
        self.nationality = nationality
        self.place_of_birth = place_of_birth
        self.ethnicity = ethnicity
        self.id_card_number = id_card_number
        self.occupation = occupation
        self.marital_status = marital_status
        self.current_address = current_address
        self.phone = phone
        self.postal_code = postal_code
        self.household_address = household_address
